// Clipping function for filter generation
static inline void clip_float(float* x) {
    if (*x > 32767.f) { *x = 32767.f; return; }
    if (*x < -32768.f) { *x = -32768.f; return; }
}

// Generate triangle waveform
static void waves_generate_triangle(int16_t* buffer, int len) {
    int d2 = len;
    int d5 = d2 >> 2;
    int d1 = 32768 / d5;
    int d4 = -(d2 >> 1);
    int16_t* edi = buffer;
    int eax = 0;

    for (int ecx = 0; ecx < d5; ecx++) {
        *edi++ = eax;
        eax += d1;
    }
    *edi++ = 0x7fff;
    if (d5 != 1) {
        eax = 32768;
        for (int ecx = 0; ecx < d5-1; ecx++) {
            eax -= d1;
            *edi++ = eax;
        }
    }
    int16_t* esi = edi + d4;
    for (int ecx = 0; ecx < d5*2; ecx++) {
        *edi++ = *esi++;
        if (edi[-1] == 0x7fff) edi[-1] = -32768;
        else edi[-1] = -edi[-1];
    }
}

// Generate square waveforms (32 variations)
static void waves_generate_square(int16_t* buffer) {
    int16_t* edi = buffer;
    for (int ebx = 1; ebx <= 0x20; ebx++) {
        for (int ecx = 0; ecx < (0x40-ebx)*2; ecx++) *edi++ = -32768;
        for (int ecx = 0; ecx < ebx*2; ecx++) *edi++ = 0x7fff;
    }
}

// Generate sawtooth waveform
static void waves_generate_sawtooth(int16_t* buffer, int len) {
    int16_t* edi = buffer;
    int ebx = 65536 / (len-1);
    int eax = -32768;
    for (int ecx = 0; ecx < len; ecx++) {
        *edi++ = eax;
        eax += ebx;
    }
}

// Generate white noise (convert 8-bit table to 16-bit)
static void waves_generate_white_noise(int16_t* buffer, int len) {
    for (int i = 0; i < len; i++) {
        int8_t sample8 = (int8_t)WhiteNoiseBig[i];
        buffer[i] = sample8 * 256;
    }
}

// Generate 31 filtered variations using 2-pole state-variable filter
static void waves_generate_filter_waveforms(int16_t* buffer, int16_t* low, int16_t* high) {
    int length_table[] = { 3, 7, 0xf, 0x1f, 0x3f, 0x7f, 3, 7, 0xf, 0x1f, 0x3f, 0x7f,
        0x7f,0x7f,0x7f,0x7f,0x7f,0x7f,0x7f,0x7f,0x7f,0x7f,0x7f,0x7f,0x7f,0x7f,0x7f,0x7f,0x7f,0x7f,0x7f,0x7f,
        0x7f,0x7f,0x7f,0x7f,0x7f,0x7f,0x7f,0x7f,0x7f,0x7f,0x7f,0x7f,
        (0x280*3)-1
    };

    for (int temp = 0, freq = 8; temp < 31; temp++, freq += 3) {
        int16_t* a0 = buffer;
        for (int waves = 0; waves < 6+6+0x20+1; waves++) {
            float fre = (float)freq * 1.25f / 100.f;
            float high_val, mid = 0.f, low_val = 0.f;
            for (int i = 0; i <= length_table[waves]; i++) {
                high_val = a0[i] - mid - low_val;
                clip_float(&high_val);
                mid += high_val * fre;
                clip_float(&mid);
                low_val += mid * fre;
                clip_float(&low_val);
            }
            for (int i = 0; i <= length_table[waves]; i++) {
                high_val = a0[i] - mid - low_val;
                clip_float(&high_val);
                mid += high_val * fre;
                clip_float(&mid);
                low_val += mid * fre;
                clip_float(&low_val);
                *low++ = (int16_t)low_val;
                *high++ = (int16_t)high_val;
            }
            a0 += length_table[waves] + 1;
        }
    }
}

// Main wave generation function
static void waves_generate(AhxWaves* waves) {
    waves_generate_sawtooth(waves->Sawtooth04, 0x04);
    waves_generate_sawtooth(waves->Sawtooth08, 0x08);
    waves_generate_sawtooth(waves->Sawtooth10, 0x10);
    waves_generate_sawtooth(waves->Sawtooth20, 0x20);
    waves_generate_sawtooth(waves->Sawtooth40, 0x40);
    waves_generate_sawtooth(waves->Sawtooth80, 0x80);
    waves_generate_triangle(waves->Triangle04, 0x04);
    waves_generate_triangle(waves->Triangle08, 0x08);
    waves_generate_triangle(waves->Triangle10, 0x10);
    waves_generate_triangle(waves->Triangle20, 0x20);
    waves_generate_triangle(waves->Triangle40, 0x40);
    waves_generate_triangle(waves->Triangle80, 0x80);
    waves_generate_square(waves->Squares);
    waves_generate_white_noise(waves->WhiteNoiseBig, 0x280*3);
    waves_generate_filter_waveforms(waves->Triangle04, waves->LowPasses, waves->HighPasses);
}

// Singleton instance
static AhxWaves* g_waves = NULL;

// Get shared waves instance
AhxWaves* ahx_waves_get(void) {
    if (!g_waves) {
        g_waves = (AhxWaves*)malloc(sizeof(AhxWaves));
        if (g_waves) {
            memset(g_waves, 0, sizeof(AhxWaves));
            waves_generate(g_waves);
        }
    }
    return g_waves;
}

// Get waveform pointer based on parameters (matches ahx_player.c:1204-1215 logic)
const int16_t* ahx_waves_get_waveform(AhxWaves* waves, uint8_t waveform, uint8_t wave_length, int filter_pos) {
    if (!waves) return NULL;

    // Clamp filter_pos to valid range (32-63)
    if (filter_pos < 32) filter_pos = 32;
    if (filter_pos > 63) filter_pos = 63;
    if (wave_length > 5) wave_length = 5;

    // White noise - no filter modulation
    if (waveform == 3) {
        return waves->WhiteNoiseBig;
    }

    // For triangle/sawtooth: Start with RAW waveform pointer, then add filter offset
    // This matches the player's WaveformTab logic (ahx_player.c:1205-1215)
    const int16_t* audio_source = NULL;
    static int wave_offsets[] = {0x00, 0x04, 0x04+0x08, 0x04+0x08+0x10, 0x04+0x08+0x10+0x20, 0x04+0x08+0x10+0x20+0x40};

    if (waveform == 0) {
        // Triangle: start at RAW Triangle04
        audio_source = waves->Triangle04 + wave_offsets[wave_length];
    } else if (waveform == 1) {
        // Sawtooth: start at RAW Sawtooth04
        audio_source = waves->Sawtooth04 + wave_offsets[wave_length];
    } else {
        return NULL;
    }

    // Add filter position offset (walks into LowPasses/HighPasses for filtered variations)
    // For FilterPos=32: offset=0, uses RAW waveform
    // For FilterPos>32: offset walks forward into filtered arrays
    audio_source += (filter_pos - 32) * (0xfc + 0xfc + 0x80*0x1f + 0x80 + 0x280*3);

    return audio_source;
}

// Generate square waveform into buffer (authentic AHX algorithm from ahx_player.c:1177-1196)
void ahx_waves_generate_square(AhxWaves* waves, int16_t* output_buffer, int square_pos,
                                uint8_t wave_length, int filter_pos, int* square_reverse) {
    if (!waves || !output_buffer) return;

    // Clamp parameters
    if (filter_pos < 32) filter_pos = 32;
    if (filter_pos > 63) filter_pos = 63;
    if (wave_length > 5) wave_length = 5;
    if (square_pos < 0) square_pos = 0;
    if (square_pos > 63) square_pos = 63;

    // Access squares with filter modulation
    // FilterPos 32 = raw squares, 33-63 = filtered variations (stored in LowPasses)
    // Within each 6520-sample filter block: triangles(252) + sawtooths(252) + squares(4096) + noise(1920)
    int16_t* square_ptr;

    if (filter_pos == 32) {
        // Use raw unfiltered squares
        square_ptr = waves->Squares;
    } else {
        // Use filtered squares from LowPasses (following ahx_player.c reference)
        // The player offsets from Squares[], which reads into LowPasses that follows in memory
        // Skip to correct filter variation, then skip past triangles and sawtooths to get to squares
        int filter_variation = filter_pos - 33;  // 0-30 for filter_pos 33-63
        int offset = filter_variation * (0xfc + 0xfc + 0x80*0x1f + 0x80 + 0x280*3) + (0xfc + 0xfc);
        square_ptr = &waves->LowPasses[offset];
    }

    // Calculate square position with wave_length scaling (from ahx_player.c:1180)
    int x = square_pos << (5 - wave_length);

    // Handle square reverse (from ahx_player.c:1182-1185)
    if (x > 0x20) {
        x = 0x40 - x;
        if (square_reverse) *square_reverse = 1;
    } else {
        if (square_reverse) *square_reverse = 0;
    }

    // OkDownSquare (from ahx_player.c:1188-1189)
    if (x > 0)
        square_ptr += (x - 1) << 7;  // Each square variation is 128 samples

    // Calculate delta for resampling (from ahx_player.c:1190)
    int delta = 32 >> wave_length;

    // Resample into output buffer (from ahx_player.c:1193-1196)
    int output_length = (1 << wave_length) * 4;
    for (int i = 0; i < output_length; i++) {
        output_buffer[i] = *square_ptr;
        square_ptr += delta;
    }
}

